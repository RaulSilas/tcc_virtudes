    public boolean verificarMes() {
        mes = jComboBoxMes.getSelectedItem().toString();

        if (mes.equalsIgnoreCase("selecione")) {
            JOptionPane.showMessageDialog(null, "Selecione um mês!", "ERRO", 0);
            prossiga = false;
        } else {
            prossiga = true;
        }

        return prossiga;
    }

    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        // Botão Salvar = Pergunta ao usuário se ele deseja salvar as alterações(chamada),
        // pega os dados salvo em vetores, e envia ao banco de dados
        int result = 0;

        if (verificarMes() == true) {
            result = JOptionPane.showConfirmDialog(null, "Deseja salvar as alterações?", "AVISO", JOptionPane.YES_NO_CANCEL_OPTION);
            if (result == JOptionPane.YES_OPTION) {
                for (int i = 0; i < jTable1.getRowCount(); i++) {
                    if (nome[i] != null && pendente[i] != false) {
                        m.setId_aluno(Integer.parseInt(conta[i]));
                        m.setMes(jComboBoxMes.getSelectedItem().toString());
                        m.setPendente(pendente[i]);
                        m.setNome(nome[i]);
                        dao.insert(m);
                    }
                }
                JOptionPane.showMessageDialog(null, "Dados inseridos!", "Sucesso", 1);
            } else {
                JOptionPane.showMessageDialog(null, "Operação cancelada!", "ERRO", 0);
            }
        }
    }//GEN-LAST:event_jButtonSalvarActionPerformed

