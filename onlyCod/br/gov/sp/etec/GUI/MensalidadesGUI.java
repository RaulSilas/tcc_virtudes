    void loadSampleData() {
        /* Centralizar o título da tabela */
        JTableHeader header = jTable1.getTableHeader();
        DefaultTableCellRenderer center = (DefaultTableCellRenderer) header.getDefaultRenderer();
        center.setHorizontalAlignment(SwingConstants.CENTER);

        /* Centralizar e alinhar a esquerda o id e o nome na tabela*/
        DefaultTableCellRenderer left = new DefaultTableCellRenderer();
        DefaultTableCellRenderer centralizado = new DefaultTableCellRenderer();
        centralizado.setHorizontalAlignment(SwingConstants.CENTER);
        left.setHorizontalAlignment(SwingConstants.LEFT);
        jTable1.getColumnModel().getColumn(0).setCellRenderer(centralizado);
        jTable1.getColumnModel().getColumn(1).setCellRenderer(left);

        /* Método para buscar os dados no banco de dados e preencher na tabela */
        try {
            Connection con = null;
            PreparedStatement ps = null;
            String sql = "SELECT id_aluno, nome FROM aluno order by nome";

            con = UtilDAO.conectar();
            ps = con.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();

            while (jTable1.getRowCount() > 0) {
                ((DefaultTableModel) jTable1.getModel()).removeRow(0);
            }
            int col = rs.getMetaData().getColumnCount();
            while (rs.next()) {
                Object[] rows = new Object[col];
                for (int i = 1; i <= col; i++) {

                    rows[i - 1] = rs.getObject(i);
                }
                ((DefaultTableModel) jTable1.getModel()).insertRow(rs.getRow() - 1, rows);
            }

            rs.close();
            ps.close();
        } catch (SQLException e) {

        }

    }

