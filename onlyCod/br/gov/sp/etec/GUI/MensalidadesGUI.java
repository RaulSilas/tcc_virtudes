    void lerCheckBox() { // Método para pegar os dados quando um checkbox estiver marcado;
        for (int i = 0; i < jTable1.getRowCount(); i++) {
            Boolean check = (Boolean) jTable1.getValueAt(i, 2); //coluna onde esta os checkbox
            if (check != null) {
                conta[i] = (String) jTable1.getValueAt(i, 0).toString();
                nome[i] = (String) jTable1.getValueAt(i, 1);
                pendente[i] = (Boolean) jTable1.getValueAt(i, 2);
            }
        }
    }

